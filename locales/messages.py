def get_text(key: str, lang: str = "en", **kwargs) -> str:
    """Get localized text"""
    if key not in MESSAGES:
        return f"[Missing: {key}]"

    msg = MESSAGES[key]

    if isinstance(msg, dict):
        text = msg.get(lang, msg.get("en", f"[Missing: {key}]"))
    else:
        text = msg

    # Format with kwargs
    if kwargs:
        try:
            text = text.format(**kwargs)
        except KeyError:
            pass

    return text


